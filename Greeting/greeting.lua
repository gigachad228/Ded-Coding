end
local color = "\27[31m"
local reset = "\27[0m"
function sinstart()
for x = 0, 3, 0.1 do
    local y = math.sin(x) * 50 + 100
    print(string.rep("/", y)..color..string.rep([[/\]], 3)..reset..string.rep([[\]], 201-y).."\n")
    end
end
function rice()
