local rand = require("./ownlibs/rand")
local socket = require("socket")
-- разбиение этой МЫШКИ в строки
local funkymouse = {}
function Funky()
  for line in mouse:gmatch("[^\r\n]+") do
    table.insert(funkymouse, line)
  end
  for stringn in ipairs(funkymouse) do
    print(funkymouse[stringn])
    local random_range = rand.random_range(1, 10) * 0.1
    os.execute("sleep " .. random_range)
  end
end

local color = "\27[31m"
local reset = "\27[0m"
function Sinstart()
  for x = 0, 3, 0.1 do
    local y = math.sin(x) * 50 + 100 - 6
    io.write(string.rep("/", y) .. color .. string.rep([[/\]], 3) .. reset .. string.rep([[\]], 213 - y - 6) .. "\n")
  end
end

function Rice()
  local x = 1
  local start_time = socket.gettime()
  while true do
    io.write("write the " .. color .. "length" .. reset .. " of the wave: ")
    local length = tonumber(io.read("*l"))
    io.write("write the frequency of the wave: ")
    local time = tonumber(io.read("*l"))
    if length == nil or time == nil then
      io.write(color .. "write a number, dumbass" .. reset .. "\n")
    else
      while true do
        local y = math.sin(x) * 50 + 100 - length
        x = x + 0.1
        while socket.gettime() - start_time < time * 0.01 do end
        start_time = socket.gettime()
        io.write(string.rep("/", y) .. color .. string.rep([[/\]], length) ..
        reset .. string.rep([[\]], 213 - y - length * 2) .. "\n")
      end
    end
  end
end

function Sex()
  os.execute("reset")
  os.execute("clear")
  print('cute + funny = cunny')
  Funky()
end

function Read()
  io.write('\n' .. "1. math.SIN()" .. "\n" .. "2. rice()" .. "\n")
  while true do
    local func = tonumber(io.read("*l"))
    if func == nil then
      io.write(color .. "write a number, dumbass" .. reset .. "\n")
    elseif func == 1 then
      Sinstart()
      break
    elseif func == 2 then
      Rice()
    elseif func > 2 then
      Sex()
      io.write('\n' .. "1. math.SIN()" .. "\n" .. "2. rice()" .. "\n")
    end
  end
end

